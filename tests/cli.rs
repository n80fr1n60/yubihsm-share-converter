// ─────────────────────── R4-5: over-determined cross-check ─────────────
//
// The default 2-of-3 yubihsm-setup ceremony has n=3, t=2, so 2t=4 and
// the round-1 H1 disjoint-subset cross-check (n ≥ 2t) is impossible.
// Round 4 (R4-5) closes that hole with a byte-wise over-determined
// Lagrange check: each EXTRA share's payload is predicted from the
// canonical polynomial (defined by shares[..t]) and compared. Single
// byte-flips in the extra shares now exit 4. In the corner case where
// n == t (no redundancy at all) `--resplit` is refused with exit 13
// BEFORE recover() runs — proving by absence-of-key-material that the
// secret never enters process memory on the refusal path.
//
// These tests use the canary `toy_2of3.txt` fixture (52-byte AES-256
// wrap-blob, see src/bin/gen_fixture.rs seed contract). The byte-35
// corruption test requires a payload of at least 36 bytes (smallest
// legitimate AES-128 wrap-blob); the toy fixture's 52 bytes satisfies
// that and exercises the over-determined Lagrange branch end-to-end.

/// Helper: load `toy_2of3.txt` and return its lines as owned strings.
/// This is the canonical synthetic 2-of-3 canary fixture.
fn load_canary_fixture_lines() -> Vec<String> {
    let raw = load_fixture("toy_2of3.txt");
    String::from_utf8(raw)
        .expect("fixture is utf-8")
        .lines()
        .filter(|l| !l.trim().is_empty())
        .map(|l| l.to_string())
        .collect()
}

