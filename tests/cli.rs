// ─────────────────────── mask-by-default ───────────────────────────────

#[test]
fn default_run_masks_aes_key_in_describe_blob() {
    // No flags → describe_blob runs but the AES key is replaced with a
    // `<redacted: ... bytes; pass --show-key to print>` marker on
    // stderr. The fabricated toy blob has wrap_id 0xa5a2 (we don't
    // assert that here; we only assert the redaction).
    //
    // stdout is also not a TTY in this child, but we passed neither
    // --resplit nor --show-key, so the gate is *not* triggered (that
    // path is just for key-bearing output). The default flow still
    // succeeds.
    let toy = load_fixture("toy_2of3.txt");
    let assert = cmd().write_stdin(toy).assert().success();

    let stderr = String::from_utf8(assert.get_output().stderr.clone()).expect("stderr is utf-8");
    assert!(
        stderr.contains("<redacted"),
        "default run must redact the AES key; got stderr:\n{stderr}"
    );
    // The toy fixture's AES key bytes (from the deterministic blob) are
    // bytes 20..52 of `(i*7)^0xA5`. The first key byte is (20*7)^0xA5
    // = 140 ^ 165 = 41 = 0x29; second is (21*7)^0xA5 = 147 ^ 165 = 50
    // = 0x32. So a leak would print "2932..." on the aes256_key line.
    // We assert the leak prefix is *absent*.
    assert!(
        !stderr.contains("aes256_key (hex)   = 2932"),
        "default run leaked the toy AES key prefix; stderr:\n{stderr}"
    );
    // Stdout must be empty in the default (no --resplit) flow.
    assert_eq!(
        assert.get_output().stdout.len(),
        0,
        "default run with no flags should produce no stdout"
    );
}

