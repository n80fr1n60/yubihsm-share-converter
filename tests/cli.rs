// ─────────────────────── R4-4: parse-error redaction ──────────────────
//
// Pre-fix, parse_legacy_share leaked the malformed input back via
// `format!("{s:?}")` style debug-prints — and the malformed input IS
// the secret share material on a typo path. Post-fix the parse-error
// machinery reports only the byte length plus a `content redacted`
// marker (and, for base64 errors, a non-content classification of the
// failure kind). The four tests below pin each leaking call site shut.

#[test]
fn parse_error_redacts_full_share_text() {
    // No-dashes line `XXXXXX` (length 6) trips the splitn(3, '-') shape
    // check at the top of parse_legacy_share. Stderr must NOT contain
    // the input string and MUST contain the length descriptor + the
    // redaction marker.
    cmd()
        .write_stdin("XXXXXX\n")
        .assert()
        .failure()
        .stderr(predicate::str::contains("XXXXXX").not())
        .stderr(predicate::str::contains("length 6"))
        .stderr(predicate::str::contains("content redacted"));
}

#[test]
fn parse_error_redacts_threshold_field() {
    // `AAAAA-2-VGhlb2RvcmU` has a non-numeric threshold of length 5.
    // Stderr must NOT contain `AAAAA` and MUST report the redacted
    // length + marker.
    cmd()
        .write_stdin("AAAAA-2-VGhlb2RvcmU\n")
        .assert()
        .failure()
        .stderr(predicate::str::contains("AAAAA").not())
        .stderr(predicate::str::contains("VGhlb2RvcmU").not())
        .stderr(predicate::str::contains("length 5"))
        .stderr(predicate::str::contains("content redacted"));
}

#[test]
fn parse_error_redacts_index_field() {
    // `2-XXXXX-VGhlb2RvcmU`: threshold parses (2), index field is
    // non-numeric `XXXXX` (length 5). Stderr must NOT contain `XXXXX`
    // and MUST report the redacted length + marker.
    cmd()
        .write_stdin("2-XXXXX-VGhlb2RvcmU\n")
        .assert()
        .failure()
        .stderr(predicate::str::contains("XXXXX").not())
        .stderr(predicate::str::contains("VGhlb2RvcmU").not())
        .stderr(predicate::str::contains("length 5"))
        .stderr(predicate::str::contains("content redacted"));
}

#[test]
fn parse_error_redacts_base64_payload() {
    // `2-1-!!!`: threshold 2 + index 1 both parse, but `!!!` is not in
    // the base64 alphabet — the decode hits InvalidByte. The base64
    // crate's own Display would emit "Invalid symbol 33, offset 0.",
    // leaking the byte value (33 = '!'). Post-fix we report only the
    // failure category + the encoded length, never the byte value.
    cmd()
        .write_stdin("2-1-!!!\n")
        .assert()
        .failure()
        // Forbidden: the literal payload bytes.
        .stderr(predicate::str::contains("!!!").not())
        // Forbidden: the base64 crate's "Invalid symbol N" leak format
        // (which would echo the offending byte's decimal value).
        .stderr(predicate::str::contains("Invalid symbol").not())
        .stderr(predicate::str::contains("Invalid last symbol").not())
        // Forbidden: the decimal byte value of '!' (33).
        .stderr(predicate::str::contains("symbol 33").not())
        // Required: the redacted length + marker + the failure kind.
        .stderr(predicate::str::contains("length 3"))
        .stderr(predicate::str::contains("content redacted"))
        .stderr(predicate::str::contains("invalid base64"));
}

